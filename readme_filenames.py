### For the code to work flawlessly, the file names must contain the following:

### - NRM demagnetation DAT file or txt file must contain "NRM"
### - ARM demagnetation DAT file or txt file must contain "ARM"
### - IRM demagnetation DAT file or txt file must contain "IRM"

### - IRM acquisition CSV or TXT files must contain "IRMacq"
### - Hysteresis CSV files must contain "Hy"
### - Bcr CSV files must contain "Bcr"

### For Thellier-Thellier experiments, the code for the temperature steps entered on the 2G must be:

### - "Z" step: temperature+".00" (e.g., 150.00)
### - "I" step: temperature+".01" (e.g., 150.01)
### - pTRM check: temperature+".02" (e.g., 150.02)
### - pTRM tail check: temperature+".03" (e.g., 150.03)